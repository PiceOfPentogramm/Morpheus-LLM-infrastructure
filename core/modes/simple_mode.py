# Простой режим работы
# Прямой ввод-вывод без обработки


class SimpleMode:
    """
    Режим 1: Простой (прямой ввод-вывод).
    
    Сообщение пользователя сразу отправляется в LLM без дополнительной обработки.
    Нет памяти, нет контекста - самый простой вариант.
    """
    
    def __init__(self, llm):
        """
        Инициализация простого режима.
        
        Параметры:
            llm: Объект LLM (внешний или локальный)
        """
        self.llm = llm
    
    def process(self, user_message, is_regeneration=False):
        """
        Обработать сообщение в простом режиме.
        
        Параметры:
            user_message: Текст сообщения от пользователя
            is_regeneration: Регенерация или новое сообщение (в простом режиме не используется)
        
        Возвращает:
            Ответ от LLM
        """
        # Просто передаём сообщение в LLM
        response = self.llm.generate(user_message)
        return response
